package Coversion_datatypes;

public class Conversion 
{
	public static void main(String args[])
	{
		byte b=(byte)128;
		short s=(short)35768;
		int i=100000;
		long l=122345;
		System.out.println("b="+b);
		System.out.println("s="+s);
		System.out.println("i="+i);
		System.out.println("l="+l);
	}
}
